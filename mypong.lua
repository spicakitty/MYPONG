t=0
INIT_STATE = 0
TITLE_STATE = 1
GAME_STATE = 2
curState = 0

getReady=true
roundOver=false
nextRoundPause=false --after roundOver
gameOver=false
gameEndPause=false --after gameOver

readyTime=-1
