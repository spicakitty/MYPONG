accel=1
menuSel=0

function initValues()
	p1y=136/2-10
	p2y=136/2-10
	ballx=118
	bally=66
end

function TIC()
	if curState==INIT_STATE then
		initValues()
		balldy=1
		balldx=-1
		curState=TITLE_STATE
	elseif curState==TITLE_STATE then
		cls()
		demo()
		title()
	elseif curState==GAME_STATE then
		game()
	end
	t=t+1
end

function drawNet()
	for _i=3,130,6 do
		rect(119,_i,2,4,14)
	end
end

function isSelCol(x)
	if x==menuSel then return 4
	else return 11 end
end

function title()
	print("MYPONG",52,26-(t%60//30),12,false,4)
	print("made by spicakitty",91,50,2,false)
	print("START GAME",92,63,isSelCol(0))
	
	local mstr="PADDLE SPEED : "..paddleSpeed
	local mtx=print(mstr,0,-10)-1
	print(mstr,(240-mtx)//2,73,isSelCol(1))
	
	mstr="BALL SPEED : "..ballSpeed
 mtx=print(mstr,0,-10)-1
	print(mstr,(240-mtx)//2,83,isSelCol(2))
	
	mstr="SPIN : "..spin
	mtx=print(mstr,0,-10)-1
	print(mstr,(240-mtx)//2,93,isSelCol(3))
	
	mstr="ACCELERATION : "..accel
	mtx=print(mstr,0,-10)-1
	print(mstr,(240-mtx)//2,103, isSelCol(4))
	
	mstr="BOUNCINESS : "..bounce
	mtx=print(mstr,0,-10)-1
	print(mstr,(240-mtx)//2,113,isSelCol(5))
	
	mstr="WIN CONDITION : "..winCondition.." POINTS"
	mtx=print(mstr,0,-10)-1
	print(mstr,(240-mtx)//2,123,isSelCol(6))
