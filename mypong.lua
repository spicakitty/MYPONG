function valMod(x)
 local isNeg
	if menuSel==1 then
	 paddleSpeed=paddleSpeed+x
		if paddleSpeed<0.5 then paddleSpeed=0.5 end
	elseif menuSel==2 then
		ballSpeed=ballSpeed+x
		if ballSpeed<0.5 then ballSpeed=0.5 end
		--if balldx<0 then isNeg = true end
		--if isNeg then
			--balldx=balldx-x
			--if balldx>-0.5 then balldx=-0.5 end
		--else
			--balldx=balldx+x
			--if balldx<0.5 then balldx=0.5 end
		--end
	elseif menuSel==3 then
		spin=spin+x
		if spin<0.5 then spin=0.5 end
		if balldy<0 then balldy=-spin
		else balldy=spin end
	elseif menuSel==4 then
		accel=accel+(x/50)
		if accel<1 then accel=1 end
		if accel>1.1 then accel=1.1 end
	elseif menuSel==5 then
		bounce=bounce+(x/50)
		if bounce<0.8 then bounce=0.8 end
		if bounce>1.1 then bounce=1.1 end
	elseif menuSel==6 then
		winCondition=winCondition+math.floor(x*2)
		if winCondition<1 then winCondition=1 end
		if winCondition>99 then winCondition=99 end
	end
end

function demo()
	drawNet()
	rect(0,0,240,1,13)
	rect(0,135,240,1,13)
	rect(0,0,1,136,13)
	rect(239,0,1,136,13)
	rect(ballx,bally,4,4,13)
	ballx=ballx+balldx*ballSpeed
	bally=bally+balldy
	if bally<=1 then coliU() end
	if bally>=132 then coliD() end
	if ballx<=1 then demoColiL() end
	if ballx>=236 then demoColiR() end
end

function game()
	cls()
	if getReady then
		readyTime=t+60
		getReady=false
	end
	
	if roundOver then
		nextRoundTime=t+150
		roundOver=false
		nextRoundPause=true
	end
	
	if gameOver then
		gameEndTime=t+300
		gameOver=false
		gameEndPause=true
		music(0,0,0,false)
	end
	
	drawNet()
	rect(0,0,239,1,13)
	rect(0,135,239,1,13)
	rect(0+4,p1y,4,20,12)
	rect(236-4,p2y,4,20,12)
	rect(ballx,bally,4,4,12)
	local mtx2=print(string.format("%02d-%02d",p1score,p2score),0,-30,14,true,2)-2
	print(string.format("%02d-%02d",p1score,p2score),(240-mtx2)//2,10,14,true,2)
	
	if t<=readyTime then
		local mtx=print("GET READY",0,-10)-1
		print("GET READY",(240-mtx)//2,50,3)
		return
	end
	
	if t<=nextRoundTime then
		return
	end
	
	if nextRoundPause then
		if p1score==winCondition or p2score==winCondition then
			gameOver=true
			nextRoundPause=false
			return
		end
		initValues()
		balldy=0
		getReady=true
		nextRoundPause=false
		return
	end
	
	if t<=gameEndTime then
		local mstr="GAME OVER"
		local mtx=print(mstr,0,-10)-1
		print(mstr,(240-mtx)//2,60,4)
		if p1score>p2score then mstr="PLAYER 1 WINS"
		else mstr="PLAYER 2 WINS" end
		mtx=print(mstr,0,-10)-1
		print(mstr,(240-mtx)//2,70,4)
		return
	end
	if gameEndPause then
		initValues()
		balldy=1
		balldx=-1
		p1score=0
		p2score=0
		curState=TITLE_STATE
		gameEndPause=false
		getReady=true
		return
	end
	
	if btn(4) then p1y=moveDown(p1y) end
	if btn(6) then p1y=moveUp(p1y) end
	if btn(0) then p2y=moveUp(p2y) end
	if btn(1) then p2y=moveDown(p2y) end
	
	ballx=ballx+balldx*ballSpeed
	bally=bally+balldy
	
	if ballx<=8 then coliL() end
	if ballx>=228 then coliR() end
	if bally<=0 then coliU() end
	if bally>=132 then coliD() end
	
	if ballx<=-4 then
		sfx(4,'C-7',-1,2)
		p2score=p2score+1
		roundOver=true
	end
	if ballx>=241 then
		sfx(5,'C-7',-1,2)
		p1score=p1score+1
		roundOver=true
	end
end

function moveUp(p)
	if p==1 then 
	else
		p=p-paddleSpeed
		if p<1 then p=1 end
	end
	return p
end

function moveDown(p)
	if y==115 then
	else
		p=p+paddleSpeed
		if p>115 then p=115 end
	end
	return p
end

function coliL()
	if ballx>=(8+balldx*ballSpeed) then
		if p1y-3<=bally and bally<=p1y+20 then
		 sfx(0,'C-6',-1,0)
			balldx=-balldx*accel
			ballx=8+(8-ballx)
			balldy=balldy+(bally-(p1y+8))*(0.2*spin)
			if balldx*ballSpeed>220 then balldx=220/ballSpeed end
		end
	end
end

function coliR()
	if ballx<=(228+balldx*ballSpeed) then
		if p2y-3<=bally and bally<=p2y+20 then
		 sfx(1,'C-6',-1,0)
			balldx=-balldx*accel
			ballx=228+(228-ballx)
			balldy=balldy+(bally-(p2y+8))*(0.2*spin)
			if balldx*ballSpeed<-220 then balldx=-220/ballSpeed end
		end
	end
end

function coliU()
	sfx(2,'G-5',-1,1)
	bally=(1-bally)+1
	balldy=-balldy*bounce
	if balldy>128 then balldy=128 end
end

function coliD()
	sfx(2,'G-5',-1,1)
	bally=131+(131-bally)
	balldy=-balldy*bounce
	if balldy<-128 then balldy=-128 end
end

function demoColiL()
	sfx(0,'C-6',-1,0)
	ballx=(1-ballx)+1
	balldx=-balldx*accel
	if balldx*ballSpeed>220 then balldx=220/ballSpeed end
end

function demoColiR()
	sfx(1,'C-6',-1,0)
	ballx=236+(236-ballx)
	balldx=-balldx*accel
	if balldx*ballSpeed<-220 then balldx=-220/ballSpeed end
end
